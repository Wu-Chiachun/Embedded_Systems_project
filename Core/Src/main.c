  macXPT2046_CS_DISABLE();
  LCD_INIT();
  HAL_ADCEx_Calibration_Start(&hadc1);
  HAL_TIM_Base_Start(&htim6);
  TimeInitPage(sec, min, hr, weekday, date, month, year, sTime, sDate, hrtc);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

	

